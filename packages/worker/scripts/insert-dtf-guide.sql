  '# DTF Transfer Application Instructions

## Overview
This guide provides step-by-step instructions for applying DTF (Direct to Film) transfers to garments using a heat press.

## Heat Press Settings

### Standard DTF Transfer Settings
- Temperature: 150-160 degrees Celsius (302-320 degrees Fahrenheit)
- Pressure: Medium to firm
- Time: 8-12 seconds
- Peel: Hot peel (peel within 2-10 seconds after pressing)

### Step-by-Step Application Process

1. Pre-press the garment to remove moisture (5 seconds)
2. Position the transfer on the garment (design side up)
3. Cover with Teflon sheet or parchment paper
4. Press for 8-12 seconds with firm pressure
5. Hot peel - peel the film away within 2-10 seconds while still warm
6. Peel film away gently at a 45-degree angle
7. Optional re-press for 5 seconds for extra durability and a matt finish

### Important Notes
- Always do a test press first on similar fabric
- Ensure the heat press is properly calibrated
- Use even, firm pressure across the entire transfer
- Do not over-press as this can damage the transfer
- Different fabrics may require slight adjustments

## Fabric-Specific Guidelines

### Cotton and Cotton Blends
- Temperature: 150-160 degrees Celsius
- Time: 10-12 seconds
- Pressure: Medium-firm

### Polyester and Synthetic Blends
- Temperature: 150-155 degrees Celsius (lower end)
- Time: 8-10 seconds
- Pressure: Medium
- Note: Use lower temperature to prevent scorching

### Nylon
- Temperature: 150 degrees Celsius
- Time: 8 seconds
- Pressure: Light-medium
- Note: Very heat sensitive - test first!

## Troubleshooting

### Transfer not sticking properly
- Increase temperature by 5 degrees
- Increase press time by 2 seconds
- Ensure firm, even pressure
- Check that garment is not moisture-laden

### Transfer peeling off after washing
- Ensure proper initial press settings
- Try re-pressing for 5 seconds after peeling
- Check wash care instructions were followed

### Scorching or discoloration
- Reduce temperature by 5-10 degrees
- Reduce press time
- Use Teflon sheet as protection

## Wash Care Instructions for Customers
- Turn garment inside out
- Wash in cold water (max 30 degrees Celsius)
- Do not bleach
- Do not tumble dry on high heat
- Iron on reverse side only
- Do not dry clean

## Storage
- Store transfers flat in a cool, dry place
- Avoid direct sunlight
- Use within 12 months for best results
- Keep away from heat sources', 
